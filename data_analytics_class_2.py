"""# **Data Preprocessing**


### **Step 1: Removing Duplicates**
"""

# Check duplicate rows
duplicate_rows = df.duplicated().sum()

print("Duplicate rows:", duplicate_rows)

# Check duplicate transaction IDs
duplicate_transaction_ids = df["transaction_id"].duplicated().sum()

print("Duplicate transaction IDs:", duplicate_transaction_ids)

"""**Why remove duplicates using transaction_id?**

> A row may not be fully identical because one field could have changed, but if two rows share the same transaction_id, they may still represent the same transaction recorded twice.

```
TX1001 | C101 | 2026-01-10 | 500 | Successful
TX1001 | C101 | 2026-01-10 | 500 | Completed
```


"""

# Display all rows with duplicate transaction IDs
df[df["transaction_id"].duplicated(keep=False)].sort_values("transaction_id")

df = df.drop_duplicates(
    subset="transaction_id",
    keep="first"
)

"""### **Step 2: Check Missing Values**"""

# Check missing values in each column
missing_values = df.isnull().sum()

print(missing_values)

# Show only columns with missing values
missing_values[missing_values > 0]

"""#### **Date**"""

# Display rows where transaction_date is missing
df[df["transaction_date"].isnull()]

"""**Why not fill the date automatically?**

> **[transaction_date]** is a critical field. Using the mean, median, or a random date would create false transaction records. In a real project, these rows would first be checked against the source system.
"""

# Remove rows with missing transaction dates
df = df.dropna(subset=["transaction_date"])

# Verify
print(df["transaction_date"].isnull().sum())

"""#### **transaction amount**"""

print(df["transaction_amount"].isnull().sum())

# Display rows where transaction_amount is missing
df[df["transaction_amount"].isnull()]

"""**`transaction_amount`** is a critical financial field, so filling it with an average could create incorrect transaction values."""

# Remove rows with missing transaction amounts
df = df.dropna(subset=["transaction_amount"])

# Verify
print(df["transaction_amount"].isnull().sum())

"""#### **Handle missing merchant_name**"""

# Display rows where merchant_name is missing
df[df["merchant_name"].isnull()]

# Fill missing merchant names
df["merchant_name"] = df["merchant_name"].fillna("Unknown")

# Verify
