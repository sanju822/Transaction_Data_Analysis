"""#### **Handle missing merchant_category**"""

# Display rows where merchant_category is missing
df[df["merchant_category"].isnull()]

# Fill missing merchant categories
df["merchant_category"] = df["merchant_category"].fillna("Unknown")

# Verify
print(df["merchant_category"].isnull().sum())

